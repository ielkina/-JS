/*Параметры функций в JavaScript
При вызове функции мы пишем ее имя и пустые круглые скобки:
*******
func();
*******
На самом деле скобки не обязательно должны быть пустыми - в них мы можем передавать 
параметры.
Пусть, к примеру, мы хотим сделать функцию, которая параметром будет принимать число
и выводить на экран квадрат этого числа.
Как мы теперь будем вызывать нашу функцию: мы будем писать имя функции, круглые скобки, 
а в них - какое-то число, квадрат которого мы хотим получить.
К примеру, вот так мы получим квадрат числа 2:*/
func(2); // выведет 4
//А вот так - квадрат числа 3:
func(3); // выведет 9
/*Давайте теперь напишем реализацию нашей функции.
Мы знаем, что функция параметром должна принимать число. Значит, при определении функции 
в круглых скобках мы должны написать какую-то переменную, в которую будет попадать 
переданное число.
Название переменной может быть любым, давайте, например, назовем ее num:*/
function func(num) {
}
//В эту переменную num при вызове функции будет попадать указанное в круглых скобках число:
func(2); // в переменную num попадет число 2
func(3); // в переменную num попадет число 3
/*Давайте теперь сделаем так, чтобы наша функция выводила квадрат переданного числа. Для этого 
перемножим переменную num саму на себя и выведем алертом на экран:*/
function func(num) {
	alert(num * num);
}
//Проверим работу функции, вызвав ее с различными числами:

function func(num) {
	alert(num * num);
}
