/*Несколько параметров
Функция может принимать не один параметр, а несколько. В этом случае их нужно перечислить через 
запятую. Давайте для примера сделаем функцию, параметром принимающую два числа и выводящую на 
экран их сумму:*/

function func(num1, num2) {
	alert(num1 + num2);
}
func(1, 2); // выведет 3

