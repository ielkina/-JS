"use strict";
/* Комбинации конструкций if-else в JavaScript
Пусть переменная num может принимать значения 1, 2 или 3.
Пусть также для каждого значения мы хотим выводить на экран свою строку, например, 
если num имеет значение 1, то выведем строку 'value1', если значение 2, то 'value2' 
и так далее.
Для решения задачи можно просто написать три if без else:*/
let num = 1; // может быть 1, 2 или 3
if (num == 1) {
    alert('value1'); // сработает, если num равно 1
}
if (num == 2) {
    alert('value2'); // сработает, если num равно 2
}
if (num == 3) {
    alert('value3'); // сработает, если num равно 3
}

/*Задача 1
В переменной day лежит какое-то число из интервала от 1 до 31. Определите в какую 
декаду месяца попадает это число (в первую, вторую или третью).*/
let day = 16;
if (day <= 10) {
    console.log('1');
}
if (day <= 20) {
    console.log('2');
}
if (day >= 30) {
    console.log('3');
}

/*Конструкция else if
Приведенный выше код можно переписать через специальную конструкцию else if:*/
let num = 1;
if (num == 1) {
    alert('value1');
} else if (num == 2) {
    alert('value2');
} else if (num == 3) {
    alert('value3');
}

