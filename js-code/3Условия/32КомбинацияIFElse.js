/*Преимущество else if
Преимуществом использования else if вместо нескольких ифов является возможность 
отловить ситуацию, когда значение переменной num не подходит ни под одно из условий:*/
let num = 1;
if (num == 1) {
    console.log('value1');
} else if (num == 2) {
    console.log('value2');
} else if (num == 3) {
    console.log('value3');
} else {
    console.log('неверное значение переменной num'); //
}

