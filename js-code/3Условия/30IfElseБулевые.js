/*А вот если в переменную test могут попадать любые значения, то все становится 
намного сложнее.
В таком случае, если в переменной test будет не булево значение, то это значение 
вначале будет преобразовано к булеву и только затем будет сравниваться.
Пусть, например, в переменной test будет число 1. В этом случае оно вначале 
преобразуется к логическому типу, то есть к true.И затем будет выполнено сравнение:*/
let test = 1;
if (test == true) {
    console.log('верно'); // сработает этот alert, тк 1 == true - это верно
} else {
    console.log('неверно');
}
/*А вот, к примеру, число 0 преобразуется к false.И наше условие в результате 
выведет 'неверно':*/
let test = 0;
if (test == true) {
    console.log('верно');
} else {
    console.log('неверно'); // сработает этот alert, тк 0 == true - это НЕверно
}
