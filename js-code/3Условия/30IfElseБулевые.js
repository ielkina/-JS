if (test == false) {
    console.log('верно');
} else {
    console.log('неверно'); //
}
/*Условие test == false на самом деле все равно, что test != true:*/
let test = true;
if (test != true) { // эквивалентно if (test == false)
    console.log('верно');
} else {
    console.log('неверно'); //
}
/*Ну, а такое условие мы уже научились сокращать в предыдущем примере.
Сократим:*/
let test = true;
if (!test) {
    console.log('верно');
} else {
    console.log('неверно'); //
}

