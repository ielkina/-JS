/*Сокращенный иф
Пусть, к примеру, мы хотим узнать, равна ли переменная test значению true.
В этом случае конструкцию
if можно записать так:*/
let test = true;
if (test == true) {
    alert('верно');
} else {
    alert('неверно');
}
/*При программировании такие проверки требуются очень часто, поэтому для них 
существует более изящная сокращенная форма: вместо
if (test == true) можно написать просто
if (test).
Давайте перепишем наш код в сокращенной форме:*/
let test = true;
if (test) { // эквивалентно if (test == true)
    console.log('верно'); //
} else {
    console.log('неверно');
}
/*Пусть теперь мы проверяем на то, что переменная test не равна true:*/
let test = true;
if (test != true) {
    console.log('верно');
} else {
    console.log('неверно'); //
}
/*В этом случае сокращенный синтаксис будет выглядеть так:*/
let test = true;
if (!test) { // используем логическое НЕ
    console.log('верно');
} else {
    console.log('неверно'); //
}
/*Аналогичное сокращение существует и для проверки на false.
Пусть дан вот такой код:*/
