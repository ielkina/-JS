"use strict";
/*Сокращенный синтаксис if-else в JavaScript
В данном уроке мы с вами изучим, какие варианты сокращений допускает конструкция 
if-else.
Необязательность конструкции else
Для начала вам следует знать, что конструкция else не является обязательной.
В следующем примере на экран выведется алерт только в том случае, если значение 
переменной равно 1. В противном случае просто ничего не случится:*/
let test = 1;
if (test == 0) {
    console.log('yes');
} else {
    console.log('no');
}
//Или более сокращенный вариант. Else  в таком условии не требуется.
let test = 1;
if (test == 0) {
    console.log('yes'); //В этом условии консоль ничего не выведет т.к условие не 
    //выполнено 
}

/*Задача 1
Если переменная test равна 10, то пусть на экран выведется слово 'верно'. 
В противном случае пусть ничего не произойдет.*/
let test = 10;
if (test) {
    console.log('yes');
} else if (test == 9) {
    console.log('no');
}

/*Необязательность фигурных скобок
В случае, если в фигурных скобках if или else будет только одно выражение, 
можно эти фигурные скобки не писать. Пусть, к примеру, дан вот такой код со 
всеми скобками:*/
if (test == 0) {
    alert('верно');
} else {
    alert('неверно!');
}
//Можно сократить его вот так:
if (test == 0) alert('верно');
else alert('неверно!');
//Или так:
if (test == 0) {
    alert('верно');
} else alert('неверно!');
//Можно также убрать все скобки, но оформить все не в линию, а вот так:
if (test == 0)
    alert('верно');
else
    alert('неверно!');

