/*Нахождение простых чисел на JavaScript
Давайте напишем код, который будет проверять число на то, простое оно или нет. 
Простое число - это число, которое не делится ни на одно другое число 
(кроме как на 1 и на само себя - на это делятся все числа).
Самый простой способ проверить число на простоту - перебрать в цикле все числа 
от 2 до самого числа и в процессе перебора проверять, делится ли наше число хотя 
бы на одно из перебираемых чисел.
Если ни на одно из этих чисел наше число не поделится - оно простое, а если хотя 
бы на одно поделится - оно составное.
Решим задачу через флаг. Давайте установим начальное значение флага в true, 
считая число по умолчанию простым:*/
