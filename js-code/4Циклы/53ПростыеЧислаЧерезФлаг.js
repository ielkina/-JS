let flag = true; // начальное значение флага
/*Затем запустим цикл от 2 до нашего числа (не включая его), и в цикле будем 
проверять, делится ли наше число на счетчик цикла:*/
let num = 31;
let flag = true;
for (let i = 2; i < num; i++) {
    if (num % i == 0) {}
}
/*Очевидно, что если num хотя бы один раз поделится на i - то число не простое. 
В таком случае мы должны установить значение флага в false:*/
let num = 31;
let flag = true;
for (let i = 2; i < num; i++) {
    if (num % i == 0) {
        flag = false; // если хотя бы один раз поделилось
    }
}
console.log(flag); // true выведем значение счетчика
/*В общем-то задача решена. Есть, однако, один нюанс: даже если в какой-то итерации 
цикла мы определим, что число не простое, цикл все равно будет крутится до самого 
конца. Это не оптимально.
Для решения проблемы добавим инструкцию break, осуществляющую выход из цикла, 
если число не простое:*/
let num = 31;
let flag = true;
for (let i = 2; i < num; i++) {
    if (num % i == 0) {
        flag = false;
        break; // выйдем из цикла
    }
}
console.log(flag);

