/*Заполнение объекта из другого объекта
Для создания объекта можно также использовать данные другого объекта. 
Пусть, к примеру, у нас есть вот такой объект:*/
/*Давайте переберем данный объект циклом for-in и запишем в новый объект 
элементы, значения которых являются четными числами:*/
let obj = { 'a': 1, 'b': 2, 'c': 3, 'd': 4, 'e': 5 };
let obj = { 'a': 1, 'b': 2, 'c': 3, 'd': 4, 'e': 5 };
let result = {};
for (let key in obj) {
    if (obj[key] % 2 == 0) {
        result[key] = obj[key];
    }
}
console.log(result); // выведет {'b': 2, 'd': 4};

