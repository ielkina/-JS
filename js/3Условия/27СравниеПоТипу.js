/*Неравенство по значению и типу
Кроме оператора != существует также оператор !==, учитывающий тип при 
сравнении. Давайте рассмотрим различия между ними на примерах.
Пусть с помощью оператора != сравниваются два числа 3. Данный оператор 
сравнивает значения на то, что они НЕ равны. Так как наши значения как 
раз-таки равны, то на экран выведется 'неверно':*/
if (3 != 3) {
    alert('верно');
} else {
    alert('неверно'); // сработает этот alert, так как значения равны
}
/*Пусть теперь одно из наших значений будет в кавычках. В этом случае 
оператор != все равно посчитает их равными (так как совпадает значение, 
а тип не важен для данного оператора) и опять выведет 'неверно':*/
if ('3' != 3) {
    alert('верно');
} else {
    alert('неверно'); // сработает этот alert, так как значения равны
}
/*Давайте теперь сравним два числа 3 с помощью оператора !==. Он также 
посчитает их равными и выведет 'неверно':*/
if (3 !== 3) {
    alert('верно');
} else {
    alert('неверно'); // сработает этот alert, так как значения равны
}
/*А вот если теперь одну из троек взять в кавычки, то оператор !== 
посчитает наши тройки неравными, так как, хотя значения их и совпадают, 
но у них разный тип:*/
if ('3' !== 3) {
    alert('верно'); // сработает этот alert, так как значения НЕ равны
} else {
    alert('неверно');
}

