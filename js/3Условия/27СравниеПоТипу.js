"use strict";
/*Типы данных и конструкция if-else в JavaScript
При использовании конструкции if можно сравнивать и строки. Для примера пусть 
в переменной test хранится какая-то строка, например 'abc':*/
let test = 'abc';
//Давайте проверим, равно ли содержимое переменной test строке 'abc':
let test = 'abc';
if (test == 'abc') {
    alert('верно'); // сработает этот alert, так как переменная равна 'abc'
} else {
    alert('неверно');
}
/*Числа в кавычках
Как вы знаете, число в кавычках представляет собой строку. Например, '3' - 
это строка. Однако, при сравнении таких строк с настоящими числами JavaScript 
считает, что строка в кавычках равна такому же числу.
Давайте для примера сравним строку '3' и число 3:*/
if ('3' == 3) {
    alert('верно'); // сработает этот alert - значения равны
} else {
    alert('неверно');
}
/*Равенство по значению и типу
Пусть вы хотите сравнивать так, чтобы число в кавычках не было равно такому 
же числу без кавычек. Говоря другими словами, вы хотите сравнивать так, чтобы 
сравнение было не только по значению, но и по типу данных.
Для этого вместо оператора == следует использовать оператор ===. 
В следующем примере при сравнении строки '3' и числа 3 на экран выведется 
'неверно', так как переменные, хотя и равны по значению, но не равны по типу:*/
if ('3' === 3) {
    alert('верно');
} else {
    alert('неверно'); // сработает этот alert
}
//А вот при сравнении двух строк '3' на экран выведется 'верно':
if ('3' === '3') {
    alert('верно'); // сработает этот alert
} else {
    alert('неверно');
}
//Так же, как и при сравнении чисел:
if (3 === 3) {
    alert('верно'); // сработает этот alert
} else {
    alert('неверно');
}
/*Разница между оператором == и оператором === проявляется именно тогда, 
когда значения одинаковые, но разный тип данных. В остальных случаях эти 
операторы работают одинаково. К примеру, при сравнении разных чисел, конечно 
же, выведется 'неверно':*/
if (2 === 3) {
    alert('верно');
} else {
    alert('неверно'); // сработает этот alert
}

