/*Результат в виде объекта
Пусть у нас опять дан наш массив:*/
let arr = ['a', 'b', 'c', 'a', 'a', 'b'];
/*Давайте подсчитаем сколько каких элементов есть в этом массиве и представим 
результат в виде объекта вот такого объекта:*/
{ a: 3, b: 2, c: 1 }
//Для решения сделаем объект count со следующим начальным значением:
{ a: 0, b: 0, c: 0 }
/*Будем перебирать массив циклом и увеличивать соответствующее значение в 
объекте count:*/
let arr = ['a', 'b', 'c', 'a', 'a', 'b'];
let count = { a: 0, b: 0, c: 0 };
for (let elem of arr) {
    count[elem]++;
}
console.log(count); // выведет {a: 3, b: 2, c: 1}
/*Пойдем дальше: то, что у объекта count в начальном значении перечислены 
символы, ведь мы обычно не знаем, какие элементы есть в массиве.
Пусть объект count формируется автоматически таким образом: если такого элемента 
в count нет, то ему следует присвоить значение 1, а если есть - просто увеличить 
значение на единицу:*/
let arr = ['a', 'b', 'c', 'a', 'a', 'b'];
let count = {};
for (let elem of arr) {
    if (count[elem] === undefined) {
        count[elem] = 1;
    } else {
        count[elem]++;
    }
}
console.log(count); // выведет {a: 3, b: 2, c: 1}

