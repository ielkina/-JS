/*Подсчет количества элементов в массиве JavaScript
Пусть дан следующий массив:*/
let arr = ['a', 'b', 'c', 'a', 'a', 'b'];
/*Давайте подсчитаем количество элементов 'a' в этом массиве. Для этого будем 
перебирать массив циклом и каждый раз, когда нам будет встречаться элемент с 
нужным значением, будем увеличивать какой-нибудь счетчик на единицу:*/
let arr = ['a', 'b', 'c', 'a', 'a', 'b'];
let counter = 0;
for (let elem of arr) {
    if (elem == 'a') {
        counter++;
    }
}
console.log(counter); //3

